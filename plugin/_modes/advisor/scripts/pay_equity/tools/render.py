# --- Renderers -------------------------------------------------------------

def _h(text: str, level: int = 2) -> str:
    return f"{'#' * level} {text}\n\n"


def _table(header: list[str], rows: list[list[str]]) -> str:
    sep = ["---" for _ in header]
    out = "| " + " | ".join(header) + " |\n"
    out += "| " + " | ".join(sep) + " |\n"
    for r in rows:
        out += "| " + " | ".join(r) + " |\n"
    return out + "\n"


def _fmt_money(v: float) -> str:
    return f"{v:,.2f}".replace(",", " ").replace(".", ",")


def _fmt_pct(v: float) -> str:
    return f"{v:.1f}%".replace(".", ",")


def _render_evaluation_summary_table(eng_dir, language: str) -> str:
    eval_path = eng_dir / "evaluation-grid.json"
    jc_path = eng_dir / "job-classes.json"
    if not eval_path.exists() or not jc_path.exists():
        raise MissingDataError(
            "evaluation-grid.json or job-classes.json missing — score classes first."
        )
    eval_file = persistence.read_json(eval_path, EvaluationFile)
    jc_file = persistence.read_json(jc_path, JobClassesFile)
    title = "Sommaire d'évaluation des catégories" if language == "fr" else "Evaluation summary by class"
    cols_label = ("Catégorie", "Titre", "Prédominance", "Total", "Qualifications", "Responsabilités", "Effort", "Conditions") if language == "fr" else ("Class", "Title", "Predominance", "Total", "Qualifications", "Responsibilities", "Effort", "Conditions")

    score_lookup = {s.class_id: s for s in eval_file.class_scores}
    sf_lookup = {sf.id: sf for sf in eval_file.grid.sub_factors}

    rows: list[list[str]] = []
    payload = []
    for cls in jc_file.classes:
        s = score_lookup.get(cls.id)
        if not s:
            continue
        by_dim: dict[str, int] = {"qualifications": 0, "responsibilities": 0, "effort": 0, "conditions": 0}
        for sid, pts in s.scores.items():
            sf = sf_lookup.get(sid)
            if sf:
                by_dim[sf.dimension] = by_dim.get(sf.dimension, 0) + int(pts)
        payload.append((cls, s.total_score, by_dim))
    payload.sort(key=lambda x: x[1], reverse=True)
    for cls, total, by_dim in payload:
        rows.append([
            cls.id,
            cls.title,
            cls.predominance.value if cls.predominance else "",
            f"{total:.0f}",
            str(by_dim.get("qualifications", 0)),
            str(by_dim.get("responsibilities", 0)),
            str(by_dim.get("effort", 0)),
            str(by_dim.get("conditions", 0)),
        ])
    return _h(title) + _table(list(cols_label), rows)


def _render_compensation_comparison_table(eng_dir, language: str) -> str:
    comp_path = eng_dir / "compensation.json"
    if not comp_path.exists():
        raise MissingDataError("compensation.json missing — call compare_compensation first.")
    comp = persistence.read_json(comp_path, CompensationFile)

    title = "Comparaison des rémunérations" if language == "fr" else "Compensation comparison"
    cols = (
        ("Catégorie féminine", "Méthode", "Comparateur", "Rém. féminine ($)", "Rém. masculine ($)", "Écart ($)", "Écart (%)", "Ajustement requis")
        if language == "fr"
        else ("Female class", "Method", "Comparator", "Female comp ($)", "Male comp ($)", "Gap ($)", "Gap (%)", "Adj. required")
    )
    rows = []
    has_unverified = False
    for r in comp.comparison_results:
        if r.data_status == "UNVERIFIED":
            has_unverified = True
            adj_label = UNVERIFIED_TAG
        else:
            adj_label = (
                ("Oui" if language == "fr" else "Yes")
                if r.requires_adjustment
                else ("Non" if language == "fr" else "No")
            )
        rows.append([
            r.female_class_id,
            r.comparison_method,
            r.male_comparator_id or "—",
            _fmt_money(r.female_compensation),
            _fmt_money(r.male_compensation),
            _fmt_money(r.gap_hourly),
            _fmt_pct(r.gap_percentage),
            adj_label,
        ])
    body = _h(title) + _table(list(cols), rows)

    # R1 H-1: surface the UNVERIFIED block when global comparison lacks a
    # supplied reference compensation. Affichage cannot claim "no gap" without
    # the prescribed-comparator tariff (RLRQ E-12.001 r.2).
    if has_unverified or (
        comp.global_comparison and comp.global_comparison.data_status == "UNVERIFIED"
    ):
        prefix = (
            f"\n{UNVERIFIED_TAG} — Comparaison globale (RLRQ E-12.001 r.2) : "
            "compensation de référence non fournie. Les écarts affichés ne peuvent "
            "pas être considérés conformes tant que le tarif prescrit n'a pas été "
            "obtenu et appliqué.\n"
            if language == "fr"
            else
            f"\n{UNVERIFIED_TAG} — Global comparison (RLRQ E-12.001 r.2): "
            "reference compensation not supplied. The gaps shown cannot be claimed "
            "compliant until the prescribed tariff is obtained and applied.\n"
        )
        body = prefix + body
    return body


def _render_adjustment_schedule_table(eng_dir, language: str) -> str:
    adj_path = eng_dir / "adjustments.json"
    if not adj_path.exists():
        raise MissingDataError("adjustments.json missing — call calculate_adjustments first.")
    adj = persistence.read_json(adj_path, AdjustmentFile)

    title_class = "Ajustements par catégorie" if language == "fr" else "Per-class adjustments"
    cols_class = (
        ("Catégorie", "Titre", "Écart hor. ($)", "Écart ann. ($)", "Effectifs", "Coût classe ($)")
        if language == "fr"
        else ("Class", "Title", "Gap/h ($)", "Annual gap ($)", "Incumbents", "Class cost ($)")
    )
    rows_class = []
    for a in adj.class_adjustments:
        rows_class.append([
            a.class_id,
            a.title,
            _fmt_money(a.gap_hourly),
            _fmt_money(a.gap_annual),
            str(a.incumbents),
            _fmt_money(a.total_class_cost),
        ])

    title_sched = "Échéancier de versement" if language == "fr" else "Payment schedule"
    cols_sched = (
        ("Année", "Date", "%", "% cumulatif", "Montant / personne ($)")
        if language == "fr"
        else ("Year", "Date", "%", "Cumulative %", "Per-employee amount ($)")
    )
    rows_sched = []
    for s in adj.payment_schedule:
        rows_sched.append([
            str(s.year),
            s.date.isoformat(),
            _fmt_pct(s.percentage),
            _fmt_pct(s.cumulative_percentage),
            _fmt_money(s.amount_per_employee),
        ])
    return (
        _h(title_class)
        + _table(list(cols_class), rows_class)
        + _h(title_sched)
        + _table(list(cols_sched), rows_sched)
    )


def _render_retroactive_calculation_table(eng_dir, language: str) -> str:
    adj_path = eng_dir / "adjustments.json"
    if not adj_path.exists():
        raise MissingDataError("adjustments.json missing — call calculate_adjustments first.")
    adj = persistence.read_json(adj_path, AdjustmentFile)

    title = "Calcul rétroactif (intérêts par période de paie)" if language == "fr" else "Retroactive calculation (per-pay-period interest)"
    note = (
        f"\n*{UNVERIFIED_TAG} : valeurs fondées sur la méthode CNESST par période de paie. "
        f"Marquage levé une fois la feuille Excel CNESST appariée.*\n\n"
        if language == "fr"
        else f"\n*{UNVERIFIED_TAG}: values use the CNESST per-pay-period method. "
        f"Tag is removed once matched against the firewalled CNESST Excel.*\n\n"
    )

    cols = (
        ("Événement", "Date", "Catégorie(s)", "Écart/h ($)", "Principal ($)", "Intérêt ($)", "Total ($)")
        if language == "fr"
        else ("Event", "Date", "Class(es)", "Gap/h ($)", "Principal ($)", "Interest ($)", "Total ($)")
    )
    rows = []
    for ev in adj.retroactive_events:
        principal = sum(b.principal_period for b in ev.interest_breakdown)
        rows.append([
            ev.event_type,
            ev.event_date.isoformat(),
            ", ".join(ev.affected_class_ids),
            _fmt_money(ev.gap_hourly_at_event),
            _fmt_money(principal),
            _fmt_money(ev.interest_accrued),
            _fmt_money(ev.total_retroactive_amount),
        ])
    out = _h(title) + note + _table(list(cols), rows)

    if adj.retroactive_events:
        sub_title = "Ventilation par période de paie" if language == "fr" else "Per-pay-period breakdown"
        sub_cols = (
            ("Événement", "Période début", "Période fin", "Heures", "Principal ($)", "Jours", "Intérêt ($)")
            if language == "fr"
            else ("Event", "Period start", "Period end", "Hours", "Principal ($)", "Days", "Interest ($)")
        )
        sub_rows = []
        for ev in adj.retroactive_events:
            for b in ev.interest_breakdown:
                sub_rows.append([
                    ev.event_type,
                    b.period_start.isoformat(),
                    b.period_end.isoformat(),
                    f"{b.hours_worked:g}",
                    _fmt_money(b.principal_period),
                    str(b.days_elapsed),
                    _fmt_money(b.interest_period),
                ])
        out += _h(sub_title) + _table(list(sub_cols), sub_rows)

    return out


def _render_obligations_summary(eng_dir, language: str) -> str:
    eng_path = eng_dir / "engagement.json"
    if not eng_path.exists():
        raise MissingDataError("engagement.json missing.")
    eng = persistence.read_json(eng_path, Engagement)
    matrix = get_obligations_matrix(eng.size_tier, eng.exercise_type)
    title = "Résumé des obligations" if language == "fr" else "Obligations summary"
    out = _h(title)
    yes = "Oui" if language == "fr" else "Yes"
    no = "Non" if language == "fr" else "No"
    rows = [
        ["Tranche / Tier" if language == "fr" else "Size tier", eng.size_tier.value],
        ["Exercice / Exercise" if language == "fr" else "Exercise type", eng.exercise_type.value],
        ["Comité / Committee" if language == "fr" else "Committee required", yes if matrix["committee_required"] else no],
        ["Comité de maintien (Bill 10)" if language == "fr" else "Maintenance committee (Bill 10)", yes if matrix["maintenance_committee_required"] else no],
        ["Affichage intermédiaire" if language == "fr" else "Interim posting", yes if matrix["interim_posting_required"] else no],
        ["Période de consultation (jours)" if language == "fr" else "Consultation period (days)", str(matrix["consultation_period_days"])],
    ]
    out += _table(["Élément" if language == "fr" else "Item", "Valeur" if language == "fr" else "Value"], rows)

    out += _h("Articles applicables" if language == "fr" else "Applicable articles", level=3)
    out += "- " + "\n- ".join(matrix["article_refs"]) + "\n\n"

    out += _h("Contenu d'affichage requis" if language == "fr" else "Required posting content", level=3)
    out += "- " + "\n- ".join(matrix["posting_content_requirements"]) + "\n\n"

    out += _h("Livrables" if language == "fr" else "Deliverables", level=3)
    out += "- " + "\n- ".join(matrix["deliverables_required"]) + "\n\n"
    return out


def _render_job_classes_table(eng_dir, language: str) -> str:
    jc_path = eng_dir / "job-classes.json"
    if not jc_path.exists():
        raise MissingDataError("job-classes.json missing.")
    jc = persistence.read_json(jc_path, JobClassesFile)
    title = "Inventaire des catégories d'emploi" if language == "fr" else "Job classes inventory"
    cols = (
        ("Catégorie", "Titre", "Effectifs", "% femmes", "Prédominance", "Méthode")
        if language == "fr"
        else ("Class", "Title", "Incumbents", "% female", "Predominance", "Method")
    )
    rows = []
    for c in jc.classes:
        rows.append([
            c.id,
            c.title,
            str(c.total_incumbents),
            _fmt_pct(c.female_percentage),
            c.predominance.value if c.predominance else "—",
            c.predominance_method.value if c.predominance_method else "—",
        ])
    return _h(title) + _table(list(cols), rows)


def _render_regression_line_summary(eng_dir, language: str) -> str:
    comp_path = eng_dir / "compensation.json"
    if not comp_path.exists():
        raise MissingDataError("compensation.json missing.")
    comp = persistence.read_json(comp_path, CompensationFile)
    if not comp.regression:
        raise MissingDataError("regression not run — compare_compensation in hybrid mode required.")
    r = comp.regression

    title = "Droite de régression masculine" if language == "fr" else "Male wage line regression"
    out = _h(title)
    rows = [
        ["Ordonnée à l'origine / Intercept" if language == "fr" else "Intercept", _fmt_money(r.intercept)],
        ["Pente / Slope" if language == "fr" else "Slope", f"{r.slope:.4f}"],
        ["R²", f"{r.r_squared:.3f}"],
        ["Points de données / Data points" if language == "fr" else "Data points", str(r.data_points)],
        ["Qualité / Quality" if language == "fr" else "Quality", r.quality_assessment],
    ]
    out += _table(["Métrique" if language == "fr" else "Metric", "Valeur" if language == "fr" else "Value"], rows)
    if r.outlier_class_ids:
        out += _h("Catégories aberrantes (résidus > 2σ)" if language == "fr" else "Outlier classes (residuals > 2σ)", level=3)
        out += "- " + "\n- ".join(r.outlier_class_ids) + "\n\n"
    if r.quality_notes:
        out += f"*{r.quality_notes}*\n\n"
    return out


